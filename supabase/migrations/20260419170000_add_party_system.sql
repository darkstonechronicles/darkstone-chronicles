create table if not exists public.parties (
  id uuid primary key default gen_random_uuid(),
  leader_user_id uuid not null references public.profiles (id) on delete cascade,
  name text not null check (char_length(btrim(name)) between 3 and 40),
  visibility text not null default 'private' check (visibility in ('private', 'open')),
  state text not null default 'forming' check (state in ('forming', 'active', 'disbanded')),
  activity text not null default 'Idle' check (char_length(btrim(activity)) between 1 and 60),
  min_level integer not null default 1 check (min_level >= 1),
  max_members integer not null default 4 check (max_members between 2 and 4),
  auto_accept_requests boolean not null default false,
  locked boolean not null default false,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  disbanded_at timestamptz
);

create table if not exists public.party_members (
  party_id uuid not null references public.parties (id) on delete cascade,
  user_id uuid not null references public.profiles (id) on delete cascade,
  role text not null default 'member' check (role in ('leader', 'member')),
  ready boolean not null default false,
  joined_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  primary key (party_id, user_id)
);

create unique index if not exists idx_party_members_user_unique
on public.party_members (user_id);

create table if not exists public.party_invites (
  id uuid primary key default gen_random_uuid(),
  party_id uuid not null references public.parties (id) on delete cascade,
  from_user_id uuid not null references public.profiles (id) on delete cascade,
  to_user_id uuid not null references public.profiles (id) on delete cascade,
  status text not null default 'pending' check (status in ('pending', 'accepted', 'declined', 'revoked', 'expired')),
  created_at timestamptz not null default timezone('utc', now()),
  expires_at timestamptz not null default (timezone('utc', now()) + interval '15 minutes'),
  responded_at timestamptz,
  metadata jsonb not null default '{}'::jsonb,
  constraint party_invites_no_self check (from_user_id <> to_user_id)
);

create unique index if not exists idx_party_invites_pending_target
on public.party_invites (party_id, to_user_id)
where status = 'pending';

create index if not exists idx_party_invites_to_user
on public.party_invites (to_user_id, status, created_at desc);

create table if not exists public.party_join_requests (
  id uuid primary key default gen_random_uuid(),
  party_id uuid not null references public.parties (id) on delete cascade,
  user_id uuid not null references public.profiles (id) on delete cascade,
  status text not null default 'pending' check (status in ('pending', 'approved', 'rejected', 'cancelled', 'expired')),
  created_at timestamptz not null default timezone('utc', now()),
  expires_at timestamptz not null default (timezone('utc', now()) + interval '15 minutes'),
  responded_at timestamptz,
  message text not null default '' check (char_length(message) <= 240)
);

create unique index if not exists idx_party_join_requests_pending_user
on public.party_join_requests (party_id, user_id)
where status = 'pending';

create index if not exists idx_party_join_requests_user
on public.party_join_requests (user_id, status, created_at desc);

create table if not exists public.party_activity_sessions (
  id uuid primary key default gen_random_uuid(),
  party_id uuid not null references public.parties (id) on delete cascade,
  activity text not null check (char_length(btrim(activity)) between 1 and 60),
  status text not null default 'active' check (status in ('active', 'completed', 'failed', 'cancelled')),
  started_by_user_id uuid not null references public.profiles (id) on delete cascade,
  member_snapshot jsonb not null default '[]'::jsonb,
  result_payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  started_at timestamptz not null default timezone('utc', now()),
  ended_at timestamptz
);

create index if not exists idx_party_activity_sessions_party
on public.party_activity_sessions (party_id, started_at desc);

create table if not exists public.party_events (
  id bigint generated by default as identity primary key,
  party_id uuid references public.parties (id) on delete cascade,
  actor_user_id uuid references public.profiles (id) on delete set null,
  event_kind text not null check (char_length(btrim(event_kind)) between 1 and 60),
  payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now())
);

create index if not exists idx_party_events_party_time
on public.party_events (party_id, created_at desc);

