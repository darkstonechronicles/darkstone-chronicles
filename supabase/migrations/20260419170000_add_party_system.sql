create or replace function public.is_party_member(p_party_id uuid, p_user_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.party_members pm
    where pm.party_id = p_party_id
      and pm.user_id = p_user_id
  );
$$;

create or replace function public.is_party_leader(p_party_id uuid, p_user_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.party_members pm
    where pm.party_id = p_party_id
      and pm.user_id = p_user_id
      and pm.role = 'leader'
  );
$$;

create or replace function public.can_view_party(p_party_id uuid, p_user_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.parties p
    where p.id = p_party_id
      and p.disbanded_at is null
      and (
        (p.visibility = 'open' and p.state = 'forming')
        or public.is_party_member(p.id, p_user_id)
        or exists (
          select 1
          from public.party_invites pi
          where pi.party_id = p.id
            and pi.to_user_id = p_user_id
            and pi.status = 'pending'
            and pi.expires_at > timezone('utc', now())
        )
        or exists (
          select 1
          from public.party_join_requests pjr
          where pjr.party_id = p.id
            and pjr.user_id = p_user_id
            and pjr.status = 'pending'
            and pjr.expires_at > timezone('utc', now())
        )
      )
  );
$$;

