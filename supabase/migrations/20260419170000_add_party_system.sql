drop policy if exists "parties_select_visible" on public.parties;
create policy "parties_select_visible"
on public.parties
for select
to authenticated
using (public.can_view_party(id, auth.uid()));

drop policy if exists "party_members_select_visible" on public.party_members;
create policy "party_members_select_visible"
on public.party_members
for select
to authenticated
using (public.can_view_party(party_id, auth.uid()));

drop policy if exists "party_invites_select_sender_or_target" on public.party_invites;
create policy "party_invites_select_sender_or_target"
on public.party_invites
for select
to authenticated
using (
  auth.uid() = from_user_id
  or auth.uid() = to_user_id
  or public.is_party_leader(party_id, auth.uid())
);

drop policy if exists "party_join_requests_select_requester_or_leader" on public.party_join_requests;
create policy "party_join_requests_select_requester_or_leader"
on public.party_join_requests
for select
to authenticated
using (
  auth.uid() = user_id
  or public.is_party_leader(party_id, auth.uid())
);

drop policy if exists "party_activity_sessions_select_visible" on public.party_activity_sessions;
create policy "party_activity_sessions_select_visible"
on public.party_activity_sessions
for select
to authenticated
using (public.can_view_party(party_id, auth.uid()));

drop policy if exists "party_events_select_members" on public.party_events;
create policy "party_events_select_members"
on public.party_events
for select
to authenticated
using (party_id is not null and public.is_party_member(party_id, auth.uid()));
